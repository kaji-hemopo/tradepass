def topic_difficulty_tag(exam_acc: float) -> str:
    """Return colour-coded difficulty emoji tag for a topic's exam accuracy."""
    if exam_acc is None:
        return ""
    if exam_acc < 50:
        return "🔴"
    if exam_acc <= 75:
        return "🟡"
    return "🟢"


def play_sound(sound_type: str = "success"):
    """Play a subtle Web Audio API tone once per results screen load.

    sound_type: 'success' (passing) or 'fail' (low score).
    Uses session_state guard so it never replays on rerun.
    """
    key = f"sound_played_{sound_type}_{st.session_state.get('page', 'unknown')}"
    if st.session_state.get(key, False):
        return
    st.session_state[key] = True

    if sound_type == "success":
        # Ascending tone: 440Hz → 880Hz over 200ms
        freq_start, freq_end, duration = 440, 880, 200
    else:
        # Descending minor tone: 440Hz → 330Hz over 250ms
        freq_start, freq_end, duration = 440, 330, 250

    st.html(f"""
<script>
(function () {{
  const ctx = new (window.AudioContext || window.webkitAudioContext)();
  const osc = ctx.createOscillator();
  const gain = ctx.createGain();
  osc.connect(gain);
  gain.connect(ctx.destination);
  osc.startTime = ctx.currentTime;
  osc.endTime = ctx.currentTime + {duration_ms} / 1000;
  osc.frequency.setValueAtTime({freq_start}, ctx.currentTime);
  osc.frequency.linearRampToValueAtTime({freq_end}, ctx.currentTime + {duration_ms} / 1000);
  gain.gain.setValueAtTime(0.15, ctx.currentTime);
  gain.gain.linearRampToValueAtTime(0, ctx.currentTime + {duration_ms} / 1000);
  osc.start();
  osc.onended = () => ctx.close();
}})();
</script>
""".format(freq_start=freq_start, freq_end=freq_end, duration_ms=duration))


