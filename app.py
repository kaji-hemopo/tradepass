def fmt_pct(val):
    if val is None:
        return "N/A"
    return f"{val:.1f}%"


def session_summary(session_type, timestamp, score_pct, accuracy_pct, topics_covered, correct_count=None, total_count=None, passed=None):
    """Build a plain-text session summary for copying/sharing."""
    lines = [
        f"📊 TradePass Study Report",
        f"━━━━━━━━━━━━━━━━━━",
        f"Type: {session_type}",
        f"When: {timestamp[:10] if timestamp else 'Just now'}",
    ]
    if correct_count is not None and total_count is not None:
        lines.append(f"Score: {score_pct:.1f}% ({correct_count}/{total_count} correct)")
    else:
        lines.append(f"Score: {score_pct:.1f}%")
    lines.append(f"Accuracy: {accuracy_pct:.1f}%")
    if passed is not None:
        lines.append(f"Result: {'✅ PASSED' if passed else '❌ FAILED'}")
    if topics_covered:
        lines.append(f"Topics: {', '.join(topics_covered)}")
    lines.append("━━━━━━━━━━━━━━━━━━")
    lines.append("Generated by TradePass — tradepass.app")
    return "\n".join(lines)


