def _score_color(pct):
    if pct is None:
        return "#888"
    if pct >= 80:
        return "#51CF66"
    if pct >= 60:
        return "#FFE066"
    return "#FF6B6B"


def _fmt_pct(val):
    if val is None:
        return "—"
    return f"{val:.0f}%"


def _type_badge(t):
    return {"exam": "📝 Exam", "focus": "🎯 Focus", "review": "📖 Review"}.get(t, t)


