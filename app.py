def page_study_plan():
    uid = st.session_state["user_id"]
    st.title("📅 Study Plan")

    with st.spinner("Loading your study plan…"):
        calendar_data = api_get(f"/api/study/calendar/{uid}")
        breakdown_data = api_get(f"/api/study/topics/{uid}/due-breakdown")

    if not calendar_data or not breakdown_data:
        st.error("Could not load study plan data.")
        return

    total_due = calendar_data.get("total_due_today", 0)
    past_weeks = calendar_data.get("past_weeks", [])
    upcoming = calendar_data.get("upcoming", [])
    topic_breakdown = breakdown_data.get("topics", []) if breakdown_data else []

    # ── Summary stats ─────────────────────────────────────────────────────
    streak_today = sum(1 for p in past_weeks[-7:] if p.get("had_activity"))
    col_s1, col_s2, col_s3 = st.columns(3)
    with col_s1:
        st.metric("🔥 Due Today", total_due)
    with col_s2:
        st.metric("📅 Active Days (7d)", streak_today)
    with col_s3:
        total_upcoming = sum(d.get("due_count", 0) for d in upcoming)
        st.metric("📬 Due Next 14d", total_upcoming)

    st.divider()

    # ── Activity Calendar: 4-week heatmap ──────────────────────────────────
    st.subheader("📆 Activity Calendar — Last 4 Weeks")

    # Group past_weeks into weeks (7 days each)
    # Display as 4 rows of 7 columns (Mon–Sun)
    DAY_LABELS = ["Mon", "Tue", "Wed", "Thu", "Fri", "Sat", "Sun"]

    # Pad start so first day aligns correctly (week starts Monday)
    # We want a 4x7 grid flattened from 28 days
    grid_days = past_weeks  # already chronological oldest→newest

    # Build grid as list of weeks
    weeks = [grid_days[i*7:(i+1)*7] for i in range(4)]

    # Header row
    header_cols = st.columns([1] + [1]*7)
    header_cols[0].write("")
    for i, day_label in enumerate(DAY_LABELS):
        header_cols[i+1].write(f"<sub>{day_label}</sub>", unsafe_allow_html=True)

    # Week rows
    streak_count = 0
    for week in weeks:
        cols = st.columns([1] + [1]*7)
        # Label week number
        cols[0].write(f"<sub>Wk</sub>", unsafe_allow_html=True)
        for i, day in enumerate(week):
            active = day.get("had_activity", False)
            questions = day.get("questions_answered", 0)
            date_label = day.get("date", "")[-5:]  # MM-DD
            # Color: green if active, grey if not
            bg = "#51cf66" if active else "#404040"
            text_color = "#ffffff" if active else "#888888"
            # Tooltip on hover via title
            cell_html = (
                f"<div title='{date_label}: {questions} Qs' "
                f"style='background:{bg};border-radius:4px;padding:6px 2px;text-align:center;color:{text_color};font-size:0.8em;font-weight:bold'>")
            if active:
                streak_count += 1
                cell_html += "●"
            else:
                cell_html += "○"
            cell_html += "</div>"
            cols[i+1].markdown(cell_html, unsafe_allow_html=True)

    st.caption(f"🔥 {streak_count}-day streak across the last 4 weeks. Green ● = active day.")

    st.divider()

    # ── Upcoming Reviews Bar Chart ─────────────────────────────────────────
    st.subheader("📬 Upcoming Reviews — Next 14 Days")

    if upcoming:
        try:
            import plotly.express as px
            import plotly.graph_objects as go

            dates = [d["date"][-5:] for d in upcoming]  # MM-DD
            due_counts = [d.get("due_count", 0) for d in upcoming]
            today_str = str(__import__('datetime').date.today())[-5:]
            colors = ["#ff6b6b" if dates[i] == today_str else "#51cf66" for i in range(len(dates))]

            fig = go.Figure()
            fig.add_bar(x=dates, y=due_counts, marker_color=colors, text=due_counts, textposition="outside")
            fig.update_layout(
                margin={"l": 0, "r": 0, "t": 10, "b": 0},
                height=220,
                plot_bgcolor="transparent",
                paper_bgcolor="transparent",
                font={"color": "#cccccc", "size": 11},
                xaxis={"tickangle": -45, "showgrid": False, "title": ""},
                yaxis={"showgrid": False, "title": ""},
            )
            st.plotly_chart(fig, use_container_width=True)
            st.caption("🔴 Today | 🟢 Upcoming days")
        except Exception:
            # Fallback: ASCII-style list
            for d in upcoming:
                today_marker = " 🔴" if d["date"] == str(__import__('datetime').date.today()) else ""
                st.write(f"{d['date'][-5:]}: {d.get('due_count',0)} cards due{today_marker}")
    else:
        st.info("No upcoming reviews scheduled.")

    st.divider()

    # ── Today's Focus ─────────────────────────────────────────────────────
    st.subheader(f"🎯 Today's Focus — {total_due} cards due")

    if not topic_breakdown:
        st.success("🎉 No cards due today! Enjoy a well-deserved break.")
    else:
        for topic in topic_breakdown:
            tid = topic.get("topic_id")
            tname = topic.get("topic_name", "Unknown")
            count = topic.get("due_count", 0)
            cols_row = st.columns([4, 1])
            with cols_row[0]:
                st.write(f"**{tname}** — `{count} cards due`")
            with cols_row[1]:
                if st.button(f"Start Review →", key=f"review_today_{tid}"):
                    st.session_state["page"] = "Study Session"
                    st.rerun()
        st.caption("All due cards are sourced from your SM-2 review queue.")


