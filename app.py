def api_headers():
    """Return Authorization header dict if token is present."""
    token = st.session_state.get("jwt_token", "")
    if token:
        return {"Authorization": f"Bearer {token}"}
    return {}


def api_get(path: str, params: dict = None):
    r = requests.get(f"{BASE_URL}{path}", headers=api_headers(), params=params, timeout=15)
    if r.status_code == 401:
        st.session_state["jwt_token"] = ""
        st.session_state["user_id"] = None
        st.error("Session expired. Please log in again.")
        st.session_state["page"] = "Login"
        return None
    if not r.ok:
        st.error(f"API error {r.status_code}: {r.text[:200]}")
        return None
    return r.json()


def api_post(path: str, payload: dict = None):
    r = requests.post(f"{BASE_URL}{path}", json=payload, headers=api_headers(), timeout=15)
    if r.status_code == 401:
        st.session_state["jwt_token"] = ""
        st.session_state["user_id"] = None
        st.error("Session expired. Please log in again.")
        st.session_state["page"] = "Login"
        return None
    if not r.ok:
        st.error(f"API error {r.status_code}: {r.text[:200]}")
        return None
    return r.json()


def api_put(path: str, payload: dict = None):
    r = requests.put(f"{BASE_URL}{path}", json=payload, headers=api_headers(), timeout=15)
    if not r.ok:
        st.error(f"API error {r.status_code}: {r.text[:200]}")
        return None
    return r.json()


