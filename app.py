def session_summary_exam(result: dict) -> str:
    """Format an Exam result as a plain-text shareable block."""
    ts = datetime.now().strftime("%Y-%m-%d %H:%M")
    score = result.get("score_percent", 0)
    passed = "✅ PASSED" if result.get("passed") else "❌ FAILED"
    correct = result.get("passed_count", 0)
    total = result.get("total_count", 0)
    breakdown = result.get("topic_breakdown", [])
    lines = [
        f"📝 TradePass Exam — {ts}",
        f"─────────────────────────────",
        f"Result: {passed} | Score: {score:.1f}%",
        f"Correct: {correct}/{total} | Pass mark: 70%",
    ]
    if breakdown:
        lines.append("Topic breakdown:")
        for b in breakdown:
            t_name = TOPIC_NAMES.get(b.get("topic_id"), f"Topic {b.get('topic_id')}")
            pct = b.get("accuracy_pct", 0)
            c = b.get("correct", 0)
            t = b.get("total", 0)
            lines.append(f"  • {t_name}: {pct:.1f}% ({c}/{t})")
    lines.append("─────────────────────────────")
    lines.append("#TradePass #ExamMode")
    return "\n".join(lines)


